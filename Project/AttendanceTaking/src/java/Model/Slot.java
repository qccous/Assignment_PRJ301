package Model;

/**
 *
 * @author duong
 */
public class Slot {
    public int id;
    public String time;

    public Slot() {
    }

    public Slot(int id, String time) {
        this.id = id;
        this.time = time;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
    
}
